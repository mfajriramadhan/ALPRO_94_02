

package ch04;


public class MenghitungSelisihDiArray {
    public static void main(String[] args) {
        int [] nilai = {82, 12, 41, 38, 19, 26, 9, 48, 20, 55, 8, 32, 3};
        System.out.println("82-12 = "+(nilai[0]-nilai[1]));
        System.out.println("41-38 = "+(nilai[1]-nilai[2]));
        System.out.println("19-26 = "+(nilai[2]-nilai[3]));
        System.out.println("9-48 = "+(nilai[3]-nilai[4]));
        System.out.println("20-55 = "+(nilai[4]-nilai[5]));
        System.out.println("8-32 = "+(nilai[5]-nilai[6]));
    }
}